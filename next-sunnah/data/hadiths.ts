// Helper function to get hadiths by chapter ID
export function getHadithsByChapter(chapterId: string): Hadith[] {
  if (chapterId.startsWith("bukhari")) {
    return bukhariHadiths.filter((hadith) => hadith.chapterId === chapterId);
  } else if (chapterId.startsWith("muslim")) {
    return muslimHadiths.filter((hadith) => hadith.chapterId === chapterId);
  } else if (chapterId.startsWith("nawawi40")) {
    return nawawi40Hadiths.filter((hadith) => hadith.chapterId === chapterId);
  }
  return [];
}

// Helper function to get a hadith by ID
export function getHadithById(hadithId: string): Hadith | undefined {
  if (hadithId.startsWith("bukhari")) {
    return bukhariHadiths.find((hadith) => hadith.id === hadithId);
  } else if (hadithId.startsWith("muslim")) {
    return muslimHadiths.find((hadith) => hadith.id === hadithId);
  } else if (hadithId.startsWith("nawawi40")) {
    return nawawi40Hadiths.find((hadith) => hadith.id === hadithId);
  }
  return undefined;
}
